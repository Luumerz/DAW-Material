let date = new Date();
console.log(date); // Fecha completa

console.log("Año:", date.getFullYear()); // Año
console.log("Mes:", date.getMonth()); // Mes (0=Enero)
console.log("Día del mes:", date.getDate()); // Día del mes
console.log("Día de la semana:", date.getDay()); // Día de la semana (0=Domingo)
console.log("Hora:", date.getHours()); // Hora
console.log("Minutos:", date.getMinutes()); // Minutos
console.log("Segundos:", date.getSeconds()); // Segundos